		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<WK.Model.sys_constant_detail> DataTableToList(DataTable dt)
		{
			List<WK.Model.sys_constant_detail> modelList = new List<WK.Model.sys_constant_detail>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				WK.Model.sys_constant_detail model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

